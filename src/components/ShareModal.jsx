export default function ShareModal({ open, onOpenChange, referralCode, coinsReward = 100 }) {
  const [copied, setCopied] = useState(false);

  // Generate the invite link
  const baseUrl = window.location.origin;
  const inviteLink = `${baseUrl}/?ref=${referralCode}`;

  // Pre-formatted share messages
  const shareMessage = `Join me on GymGraph - the fitness app that rewards you for working out! Use my code ${referralCode} and we both get ${coinsReward} coins! ${inviteLink}`;
  const shortMessage = `Use code ${referralCode} on GymGraph and we both get ${coinsReward} coins!`;

  const copyLink = async () => {
    try {
      await navigator.clipboard.writeText(inviteLink);
      setCopied(true);
      toast.success("Invite link copied!");
      setTimeout(() => setCopied(false), 2000);
    } catch {
      toast.error("Failed to copy link");
    }
  };

  const copyCode = async () => {
    try {
      await navigator.clipboard.writeText(referralCode);
      toast.success("Referral code copied!");
    } catch {
      toast.error("Failed to copy code");
    }
  };

  const shareViaWhatsApp = () => {
    const url = `https://wa.me/?text=${encodeURIComponent(shareMessage)}`;
    window.open(url, '_blank');
  };

  const shareViaTelegram = () => {
    const url = `https://t.me/share/url?url=${encodeURIComponent(inviteLink)}&text=${encodeURIComponent(shortMessage)}`;
    window.open(url, '_blank');
  };

  const shareViaSMS = () => {
    const url = `sms:?body=${encodeURIComponent(shareMessage)}`;
    window.location.href = url;
  };

  const shareNative = async () => {
    if (navigator.share) {
      try {
        await navigator.share({
          title: 'Join GymGraph',
          text: shortMessage,
          url: inviteLink,
        });
      } catch (err) {
        if (err.name !== 'AbortError') {
          toast.error("Failed to share");
        }
      }
    } else {
      copyLink();
    }
  };

  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="sm:max-w-md bg-white border-[#E5E7EB] rounded-3xl p-0 overflow-hidden">
        <div className="bg-gradient-to-br from-[#0066FF] to-[#0052CC] p-6 text-white">
          <DialogHeader>
            <DialogTitle className="text-xl font-bold text-white flex items-center gap-2">
              <Gift className="w-6 h-6" />
              Invite Friends
            </DialogTitle>
          </DialogHeader>
          <p className="text-white/80 text-sm mt-2">
            Share your code and you both get <span className="font-bold text-[#FFD700]">{coinsReward} coins</span> when they sign up!
          </p>
        </div>

        <div className="p-6 space-y-6">
          {/* Referral Code Display */}
          <div className="text-center">
            <p className="text-[#888888] text-sm mb-2">Your Referral Code</p>
            <div className="flex items-center justify-center gap-3">
              <div className="px-6 py-3 bg-[#F8F9FA] rounded-2xl border-2 border-dashed border-[#E5E7EB]">
                <p className="text-2xl font-bold font-mono tracking-widest text-[#111111]">
                  {referralCode || "--------"}
                </p>
              </div>
              <Button
                variant="outline"
                size="icon"
                onClick={copyCode}
                className="h-12 w-12 rounded-xl border-[#E5E7EB] hover:border-[#0066FF] hover:bg-[#E6F0FF]"
              >
                <Copy className="w-5 h-5 text-[#555555]" />
              </Button>
            </div>
          </div>

          {/* Share Options */}
          <div>
            <p className="text-[#888888] text-sm mb-3 text-center">Share via</p>
            <div className="grid grid-cols-4 gap-3">
              <button
                onClick={shareViaWhatsApp}
                className="flex flex-col items-center gap-2 p-4 rounded-2xl bg-[#25D366]/10 hover:bg-[#25D366]/20 transition-colors group"
              >
                <div className="w-12 h-12 bg-[#25D366] rounded-xl flex items-center justify-center group-hover:scale-110 transition-transform">
                  <WhatsAppIcon className="w-6 h-6 text-white" />
                </div>
                <span className="text-xs font-medium text-[#555555]">WhatsApp</span>
              </button>

              <button
                onClick={shareViaTelegram}
                className="flex flex-col items-center gap-2 p-4 rounded-2xl bg-[#0088cc]/10 hover:bg-[#0088cc]/20 transition-colors group"
              >
                <div className="w-12 h-12 bg-[#0088cc] rounded-xl flex items-center justify-center group-hover:scale-110 transition-transform">
                  <TelegramIcon className="w-6 h-6 text-white" />
                </div>
                <span className="text-xs font-medium text-[#555555]">Telegram</span>
              </button>

              <button
                onClick={shareViaSMS}
                className="flex flex-col items-center gap-2 p-4 rounded-2xl bg-[#34C759]/10 hover:bg-[#34C759]/20 transition-colors group"
              >
                <div className="w-12 h-12 bg-[#34C759] rounded-xl flex items-center justify-center group-hover:scale-110 transition-transform">
                  <MessageCircle className="w-6 h-6 text-white" />
                </div>
                <span className="text-xs font-medium text-[#555555]">SMS</span>
              </button>

              <button
                onClick={shareNative}
                className="flex flex-col items-center gap-2 p-4 rounded-2xl bg-[#0066FF]/10 hover:bg-[#0066FF]/20 transition-colors group"
              >
                <div className="w-12 h-12 bg-[#0066FF] rounded-xl flex items-center justify-center group-hover:scale-110 transition-transform">
                  <Share2 className="w-6 h-6 text-white" />
                </div>
                <span className="text-xs font-medium text-[#555555]">More</span>
              </button>
            </div>
          </div>

          {/* Copy Link Button */}
          <div className="pt-2">
            <Button
              onClick={copyLink}
              className={`w-full h-14 rounded-xl font-semibold text-base transition-all ${
                copied
                  ? 'bg-[#00C853] hover:bg-[#00C853] text-white'
                  : 'bg-[#F8F9FA] hover:bg-[#E6F0FF] text-[#111111] border border-[#E5E7EB]'
              }`}
            >
              {copied ? (
                <>
                  <Check className="w-5 h-5 mr-2" />
                  Link Copied!
                </>
              ) : (
                <>
                  <Link2 className="w-5 h-5 mr-2" />
                  Copy Invite Link
                </>
              )}
            </Button>
          </div>

          {/* Reward Info */}
          <div className="flex items-center justify-center gap-2 text-[#888888] text-sm">
            <Coins className="w-4 h-4 text-[#FFD700]" />
            <span>Both you and your friend get {coinsReward} coins</span>
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
}
